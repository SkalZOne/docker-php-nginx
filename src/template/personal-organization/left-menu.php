                $(document).ready(function() {
                    // Обработчик клика на кнопку
                    $('.adm-sidebar-accordion-btn').on('click', function() {
                        var $accordionItem = $(this).closest('.adm-sidebar-accordion-item'); // Находим ближайший родительский элемент
                        var $content = $accordionItem.find('.sidebar-accordion-content'); // Находим соответствующий блок контента

                        // Проверяем, имеет ли кнопка уже классы active и контент класс open
                        if ($(this).hasClass('active') && $content.hasClass('open')) {
                            // Если да, убираем эти классы
                            $(this).removeClass('active');
                            $content.removeClass('open');
                        } else {
                            // Если нет, сначала убираем классы с других элементов
                            $('.adm-sidebar-accordion-btn').removeClass('active');
                            $('.sidebar-accordion-content').removeClass('open');

                            // Добавляем классы только для выбранного
                            $(this).addClass('active');
                            $content.addClass('open');
                        }
                    });
